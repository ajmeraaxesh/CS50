#include <stdio.h>

int main()
{
  printf("char: %ld\n",sizeof(char));
  printf("double: %ld\n",sizeof(double));
  printf("float: %ld\n",sizeof(float));
  printf("int: %ld\n",sizeof(int));
  printf("long: %ld\n",sizeof(long));
  printf("long long: %ld\n",sizeof(long long));
}
